/**
 * Configuration de validation pour les vidéos
 */
export const VIDEO_CONFIG = {
  MAX_FILE_SIZE: 100 * 1024 * 1024, // 100 MB
  MAX_DURATION: 600, // 10 minutes en secondes
  ALLOWED_FORMATS: ["video/mp4", "video/webm", "video/quicktime"],
  THUMBNAIL_WIDTH: 320,
  THUMBNAIL_HEIGHT: 568,
};

/**
 * Interface pour les métadonnées vidéo
 */
export interface VideoMetadata {
  filename: string;
  duration: number;
  size: number;
  mimeType: string;
  uploadedAt: Date;
}

/**
 * Valide un fichier vidéo avant upload
 */
export function validateVideoFile(file: File): { valid: boolean; error?: string } {
  // Vérifier le type MIME
  if (!VIDEO_CONFIG.ALLOWED_FORMATS.includes(file.type)) {
    return {
      valid: false,
      error: `Format vidéo non supporté. Formats acceptés: MP4, WebM, MOV`,
    };
  }

  // Vérifier la taille
  if (file.size > VIDEO_CONFIG.MAX_FILE_SIZE) {
    return {
      valid: false,
      error: `Fichier trop volumineux. Taille maximale: 100 MB`,
    };
  }

  return { valid: true };
}

