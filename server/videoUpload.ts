/**
 * Upload une vidéo vers Supabase Storage
 */
export async function uploadVideoToStorage(
  file: File,
  userId: number
): Promise<{ videoUrl: string; thumbnailUrl: string; size: number }> {
  // Valider le fichier
  const validation = validateVideoFile(file);
  if (!validation.valid) {
    throw new Error(validation.error);
  }

  // Générer un ID unique pour la vidéo
  const videoId = uuidv4();
  const fileExtension = file.name.split(".").pop() || "mp4";
  const videoKey = `videos/${userId}/${videoId}.${fileExtension}`;

  // Convertir le fichier en buffer
  const arrayBuffer = await file.arrayBuffer();
  const buffer = Buffer.from(arrayBuffer);

  // Upload la vidéo
  const { url: videoUrl } = await storagePut(videoKey, buffer, file.type);

  // Générer la miniature
  const thumbnailUrl = await generateVideoThumbnail(buffer, videoId);

  return {
    videoUrl,
    thumbnailUrl,
    size: file.size,
  };
}

/**
 * Extrait la durée d'une vidéo (côté client)
 */
export async function getVideoDuration(file: File): Promise<number> {
  return new Promise((resolve, reject) => {
    const video = document.createElement("video");
    const objectUrl = URL.createObjectURL(file);

    video.onloadedmetadata = () => {
      URL.revokeObjectURL(objectUrl);
      resolve(Math.round(video.duration));
    };

    video.onerror = () => {
      URL.revokeObjectURL(objectUrl);
      reject(new Error("Failed to load video metadata"));
    };

    video.src = objectUrl;
  });
}
